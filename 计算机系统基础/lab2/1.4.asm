.386
STACK SEGMENT USE16 STACK
    DB 200 DUP(0)
STACK ENDS
DATA SEGMENT USE16	

CRLF DB 0DH,0AH,'$'
COUNT1 = $-BNAME
COUNT2 = $-BPASS
N EQU 30
S1  DB 'SHOP_ONE',0
GA1 DB 'PEN',7 DUP(0),10
    DW 35,56,70,25,?
GA2 DB 'BOOK',6 DUP(0),9
    DW 12,30,25,5,?
GAN DB N-2 DUP('TEMP-VALUE',8,15,0,20,0,30,0,2,0,?,?)
	
DATA ENDS

CODE SEGMENT USE16
    ASSUME CS:CODE,DS:DATA,SS:STACK
MAIN:
	MOV AX, DATA
	MOV DS,AX
	LEA DI,GA1
	MOV CX,10

	LEA DX,BUF
	MOV AH,9
	INT 21H
	LEA DX,CRLF
	MOV AH,9
	INT 21

LL:
	LEA DI,GA1
	IMUL DX,21
	ADD DI,DX
	LEA SI,IN_GOOD
	MOV CL,1[SI]
	
	
L_OUT_A: 
	MOV DL,'A'
	MOV AH,2
	INT 21H
	LEA DX,CRLF
	MOV AH,9
	INT 21H
	JMP L_IN_NAME
L_OUT_B:
	MOV DL,'B'
	MOV AH,2
	INT 21H
	LEA DX,CRLF
	MOV AH,9
	INT 21H
	JMP L_IN_NAME
L_OUT_C:
	MOV DL,'C'
	MOV AH,2
	INT 21H
	LEA DX,CRLF
	MOV AH,9
	INT 21H
	JMP L_IN_NAME
L_OUT_F:
	MOV DL,'F'
	MOV AH,2
	INT 21H
	LEA DX,CRLF
	MOV AH,9
	INT 21H
	JMP L_IN_NAME
OVER: 
	MOV AH,4CH
	INT 21H
CODE ENDS 
	END MAIN
